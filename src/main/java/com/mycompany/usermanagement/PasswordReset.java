    private void btnEnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnterActionPerformed
        // TODO add your handling code here:
        if(isEmailSent==false){ //API
            gmailservice gmail = new gmailservice();
            boolean success = gmail.sendOTPEmail(email);
            
            if(!success){
                JOptionPane.showMessageDialog(this, "Failed to send OTP email. Please try again.");
                return;
            }
            
            String inputOtpStr = JOptionPane.showInputDialog(
                    this,
                    "An OTP has been sent to your email.\nPlease enter it below:",
                    "OTP Verification",
                    JOptionPane.QUESTION_MESSAGE
            );
            
            if(inputOtpStr == null){
                return;
            }
            
            try{
                int inputOtp = Integer.parseInt(inputOtpStr);
                int otp = gmail.getCurrentOtp();
                if(inputOtp == otp){
                    JOptionPane.showMessageDialog(this, "OTP verified! You can now reset your password.");
                    lbltop.setText("New Password");
                    pf1.setVisible(true);
                    lblPassword2.setVisible(true);
                    lblPassword2.setText("Confirm Password");
                    pf2.setVisible(true);
                    btnEnter.setText("Change Password");
                    isEmailSent = true;
                    isEmailSent = true;
                }else{
                    JOptionPane.showMessageDialog(this, "Incorrect OTP. Please try again.");
                }
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(this, "Invalid OTP format. Please enter numbers only.");
            }
            
        }else{
            String pass1 = new String(pf1.getPassword());
            String pass2 = new String(pf2.getPassword());
            
            if (pass1 == null || pass1.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter The New Password");
            }else if(pass1.equals(pass2)){                
                User logged = SessionManager.getLoggedUser();
                String oldPass = logged.getPassword();
                logged.setPassword(pass1);
                
                UserFileWriter writer = new UserFileWriter("account_information.csv");
                
                boolean result = writer.updateUserByID(logged.getUserID(),logged);
                
                if(result){
                    JOptionPane.showMessageDialog(
                        null,
                        "Your password has been successflly updated.",
                        "Update Successful",
                        JOptionPane.INFORMATION_MESSAGE
                    );
                    
                    this.dispose();
                    new UserInfo().setVisible(true);
                    
                }else{
                    logged.setPassword(oldPass);
                    
                    JOptionPane.showMessageDialog(
                        null,
                        "Failed to update your password.\nPlease try again later",
                        "Update Failed",
                        JOptionPane.ERROR_MESSAGE
                    );
                    
                }
            }else{
                pf2.setText("");
                lblRed.setText("The passwords you entered do not match. Please try again.");
            }
            
            
        }
    }//GEN-LAST:event_btnEnterActionPerformed

