    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        // TODO add your handling code here:
        UserFileWriter writer = new UserFileWriter("account_information.csv");
        User newUser = null;
        
        if (role.equals("Student")) {
            newUser = new Student(userID, firstName, lastName, email, password, role, true, lastLogin, major, year);
        }else if (role.equals("Officer")) {
            newUser = new Officer(userID, firstName, lastName, email, password, role, true, lastLogin);
        }else if (role.equals("Admin")) {
            newUser = new Admin(userID, firstName, lastName, email, password, role, true, lastLogin);
        }
        
        if(newUser != null){
            boolean result = writer.appendUser(newUser);
            
            if (result) {
                JOptionPane.showMessageDialog(this, "New user has been created successfully.");
            }else{
                JOptionPane.showMessageDialog(this, "Failed to add user.\nPlease try again later.");
            }
            
            txtFirstName.setText("");
            txtLastName.setText("");
            cmbRole.setSelectedIndex(0);
            cmbMajor.setSelectedIndex(0);
            cmbYear.setSelectedIndex(0);
            cardLayout.show(pnlMain, "inputPage");
        }
    }//GEN-LAST:event_btnOKActionPerformed

