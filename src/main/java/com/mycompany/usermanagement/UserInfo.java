    private void displayUserData(){
        if(loggedUser != null){
            lblUserName.setText("Name: " + loggedUser.getFirstName() + " " + loggedUser.getLastName());
            lblUserID.setText("User ID: " + loggedUser.getUserID());
            lblUserEmail.setText("Email :" + loggedUser.getEmail());
            lblUserPassword.setText("Password :" + "*".repeat(loggedUser.getPassword().length()));
            lblLastLogin.setText("Last Login : " + loggedUser.getLastLogin());
            
            if(loggedUser instanceof Student student){
                lblStudentMajorYear.setVisible(true);
                lblStudentMajorYear.setText("Major :" + student.getMajor() + "(" + student.getYear() +")");
            }else{
                lblStudentMajorYear.setVisible(false);
            }
        }
    }

